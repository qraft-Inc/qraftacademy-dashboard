import Head from "next/head";
import { useState } from "react";
import logo from "../public/images/logo.png";
import Image from "next/image";
import Footer from "../components/Footer";
import axios from "axios";
import { toast } from "react-toastify";
import { Formik, Field, Form } from "formik";

export default function ForgotPassword() {
  const [error, setError] = useState("");

  // initial form value
  const initialValues = {
    user: {
      email: "",
    },
  };

  const onSubmit = async (values, onSubmitProps) => {
    try {
      const { data } = await axios.post(`/api/auth/forgot-password`, values);
      console.log(data);

      toast.success(data.message, {
        position: "top-center",
        theme: "colored",
        autoClose: 2000,
      });
      onSubmitProps.resetForm();
    } catch (error) {
      setError(error.response.data.error);
      onSubmitProps.resetForm();
      setTimeout(() => {
        setError("");
      }, 5000);
    }
  };
  return (
    <>
      <Head>
        <title>forgot-Password</title>
        <meta name="description" content="Generated by create next app" />
      </Head>
      <div className="flex flex-col h-screen justify-between">
        <div className="flex justify-start p-4">
          <Image
            alt="profile pic"
            src={logo}
            className=""
            width={90}
            height={62}
            placeholder="blur"
            blurDataURL
          />
        </div>
        <div className="flex flex-col">
          <div className="conatiner mx-auto flex items-center">
            <div className="px-4 max-w-md sm:w-full space-y-8 text">
              <h2 className="text-center text-3xl font-extrabold text-gray-900">
                Forgot Password
              </h2>
              <span className="text-center text-sm font-normal">
                Please enter your email address.You will receive a link to
                create a new password.
              </span>

              <Formik
                initialValues={initialValues}
                onSubmit={onSubmit}
                className="space-y-2"
              >
                <Form>
                  <div className="rounded-md shadow-sm">
                    <div>
                      <Field
                        className="shadow appearance-none border rounded w-full py-2 px-3 text-grey-darker"
                        type="email"
                        aria-label="email"
                        name="user.email"
                      />
                    </div>
                  </div>
                  <div className="flex flex-col">
                    <button
                      type="submit"
                      className="rounded-lg bg-[#4092CF] text-base px-3 py-2 hover:bg-blue-400 transition duration-300"
                    >
                      Send Email
                    </button>
                    {error && <span className="text-red-500">{error}</span>}
                  </div>
                </Form>
              </Formik>
            </div>
          </div>
        </div>
        <Footer />
      </div>
    </>
  );
}
